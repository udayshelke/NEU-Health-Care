  private void populateNetworkSystemTable() {
    DefaultTableModel model = (DefaultTableModel) tblNetworkSystem.getModel();
    model.setRowCount(0);
    for (NetworkSystem ns : ecosystem.getNetworkSystems()) {
      Object[] row = new Object[2];
      row[0] = ns;
      row[1] = ns.getZipcode();
      model.addRow(row);
    }
  }
